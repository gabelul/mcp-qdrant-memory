// Color codes for better terminal output
const colors = {
  reset: '\x1b[0m',
  red: '\x1b[31m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m',
  cyan: '\x1b[36m',
  bold: '\x1b[1m'
};

/**
 * Console logging with colors
 */
function log(message, color = 'reset') {
  console.log(`${colors[color]}${message}${colors.reset}`);
}

/**
 * Create readline interface for user input
 */
function createInterface() {
  return readline.createInterface({
    input: process.stdin,
    output: process.stdout
  });
}

/**
 * Prompt user for input with default value
 */
function askQuestion(rl, question, defaultValue = '') {
  return new Promise((resolve) => {
    const prompt = defaultValue 
      ? `${question} (${colors.cyan}${defaultValue}${colors.reset}): `
      : `${question}: `;
    
    rl.question(prompt, (answer) => {
      resolve(answer.trim() || defaultValue);
    });
  });
}

/**
 * Check if a file exists and is readable
 */
function fileExists(filePath) {
  try {
    fs.accessSync(filePath, fs.constants.R_OK);
    return true;
  } catch {
    return false;
  }
}

/**
 * Load existing configuration from various sources
 */
function loadExistingConfig() {
  const possiblePaths = [
    path.join(process.cwd(), 'settings.txt'),
    path.join(__dirname, '../../settings.txt'),
    path.join(__dirname, '../../../settings.txt'),
    path.join(os.homedir(), 'settings.txt'),
    path.join(process.cwd(), '.env'),
    path.join(__dirname, '../../.env')
  ];

  for (const configPath of possiblePaths) {
    if (fileExists(configPath)) {
      try {
        const content = fs.readFileSync(configPath, 'utf8');
        const config = {};
        
        // Parse key=value pairs
        content.split('\n').forEach(line => {
          const cleanLine = line.trim();
          if (cleanLine && !cleanLine.startsWith('#')) {
            const [key, ...valueParts] = cleanLine.split('=');
            if (key && valueParts.length > 0) {
              config[key.trim()] = valueParts.join('=').trim();
            }
          }
        });
        
        if (Object.keys(config).length > 0) {
          log(`âœ… Found existing configuration at: ${configPath}`, 'green');
          return { config, path: configPath };
        }
      } catch (error) {
        log(`âš ï¸ Could not read ${configPath}: ${error.message}`, 'yellow');
      }
    }
  }
  
  return null;
}

